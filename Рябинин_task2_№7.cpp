#include<iostream>
#include <math.h>
using namespace std;

void main()
{	
	setlocale(LC_ALL, "Russian");

	//Цикл позволяет сделать меню для программы
	while (true)
	{	//Переменная а служит выходом из цикла, если она равна 0, иначе цикл выполняется
		int a;
		cout << "Выберете действие 0 - выход, 1 - вычесление: " << endl;
		cin >> a;

		if (a == 0)
		{
			break;
		}
		else
		{	//цикл позволяет сделать еще одно меню
			while (true)
			{	// Переменная z делает первод 1) при ее значении 1, иначе делает перевод 2)
				int z;
				cout << "Выберете действие 1) - из декартовой в полярную, 2) - из полярной в декартовую: " << endl;
				cin >> z;
				if (z == 1)
				{	//Переменные х и у обозначают ось абсцисс и ось ординат соответственно
					double x, y;
					cout << "Введите x " << endl;
					cin >> x;

					cout << "Введите y " << endl;
					cin >> y;
					// Переменная r отвечает за полярный радиус, переменная к за полярный угол
					double r;
					r = sqrt(x * x + y * y);
					double k;
					k = atan(y / x) * 180 / 3.1415;

					cout << "Полярный радиус = " << r << " " << "Полярный угол = " << k << endl;

					break;

				}
				else
				{	// Переменная r отвечает за полярный радиус, переменная к за полярный угол
					double r, k;
					cout << "Введите полярный радиус " << endl;
					cin >> r;

					cout << "Введите полярный угол " << endl;
					cin >> k;
					k = k * 3.1415 / 180;
					//Переменные х и у обозначают ось абсцисс и ось ординат соответственно
					double x, y;

					x = r * cos(k);
					y = r * sin(k);

					cout << "x = " << x << " " << "y = " << y << endl;

					break;

				}
			}
		}




	}
}